def conwaypol(p,n):
    """list of coefficients of the primitive conway polynomial of a field of p^n elements. Here p is a prime, n a positive integer and [a0, a1, ..., 1] is a list of integers such that the Conway polynomial fp,n(X) = (a0 mod p) + (a1 mod p) X + (a2 mod p) X2 + ...+ Xn."""
    if p>23 or n>24:
        print("p,n not found. visit:") 
        print("http://www.math.rwth-aachen.de/~Frank.Luebeck/data/ConwayPol/CPimport.txt")
    else:
        for poly in ConwayPolynomials:
            if poly[0]==p:
                if poly[1]==n:
                    return poly[2]
        else:
            print("p,n not found. visit:")
            print("http://www.math.rwth-aachen.de/~Frank.Luebeck/data/ConwayPol/CPimport.txt")


